/*
===========================
Insertion Sort – Simple Notes
===========================

Overview:
- Comparison-based sorting algorithm
- Builds sorted list one element at a time
- Inserts element into correct position
- Works like sorting playing cards
- Good for small or nearly sorted data

Time Complexity:
- Best: O(n)
- Average: O(n^2)
- Worst: O(n^2)

Space Complexity:
- O(1)

Very Simple Example:

Array:
[3, 1, 2]

Step 1:
Take 1 and insert before 3
[1, 3, 2]

Step 2:
Take 2 and insert in correct place
[1, 2, 3]

Final Sorted Array:
[1, 2, 3]
*/
